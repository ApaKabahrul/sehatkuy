                        <h2 class="blog-ttl">Manfaat Olahraga bagi Kesehatan Mental</h2>
                        
                        <div class="blog-post">
                            <p class="paragraph-medium paragraph-black">Semua orang tahu bahwa manfaat olahraga yang utama adalah membuat tubuh lebih sehat. 
				Selain tubuh yang sehat, tahukah kita bahwa khasiat olahraga juga baik untuk kesehatan mental?</p>

                            <p class="paragraph-medium paragraph-black">Meski banyak orang sudah tahu mengenai khasiat olahraga, beberapa orang masih enggan untuk melakukannya.
				Beberapa kondisi yang membuat masyarakat enggan melakukan olahraga karena padatnya aktivitas sehari-hari.</p>
				<p class="paragraph-medium paragraph-black">Melewatkan waktu berolahraga adalah cara hidup yang tidak sehat di mana keseimbangan aktivitas dengan pola 
				makan dan tidak adanya olahraga bisa mengakibatkan tidak adanya keseimbangan di dalam tubuh.</p>
				
				<p class="paragraph-medium paragraph-black">Kondisi ini mengakibatkan daya tahan tubuh akan menurun dan pola makan yang tidak teratur. Apalagi ditambah 
				dengan pola makanan yang tidak sehat di mana bisa mengakibatkan munculnya penyait-penyakit di dalam tubuh.</p>
                            <div class="theme-quote">
                                <i class="fa fa-quote-left"></i> Menurut Daniel M. Landers, profesor ilmu kesehatan fisik dan olahraga dari Univeritas Arizona, manfaat olahraga yang bisa didapatkan tubuh tidak hanya terkait dengan kondisi fisik, menurutnya manfaat olahraga yang bisa dirasakan 
				adalah membuat pikiran lebih jernih dan mengurangi ketegangan.</p>
				
                            </div>
                            <p class="paragraph-medium paragraph-black">Landers mengungkapkan terdapat lima alasan kenapa olahraga baik untuk kesehatan mental, di antaranya:</p>
                            <p class="paragraph-medium paragraph-black">1. Olahraga mengurangi stres</p>
				
				<p class="paragraph-medium paragraph-black">Setiap manusia normal pernah mengalami stress atau ketegangan, dengan berolahraga dapat membantu tubuh mengurangi kegelisahan hingga meredam kemarahan.</p>
				<p class="paragraph-medium paragraph-black">Alasannya, saat jantung kita bekerja pada saat berolahraga, maka otomatis konsentrasi pikiran tidak akan terfokus pada urusan yang membuat kita stress. 
				Selain dapat mengalihkan pikiran, manfaat olahraga seperti aerobik juga dapat meningkatkan ketahanan kardiovaskular, sehingga nantinya kita dapat bersikap 
				tidak terlalu berlebihan dalam menyikapi suatu masalah.</p>
				<p class="paragraph-medium paragraph-black">Aktivitas yang terbukti efektif dalam melawan ketegangan otak adalah berjalan kaki, bersepeda, renang, joging dan yoga.</p>
				
				<p class="paragraph-medium paragraph-black ">2. Olahraga dapat meningkatkan kekuatan otak</p>
				
				<p class="paragraph-medium paragraph-black">Setiap kegiatan fisik -dalam hal ini olahraga- yang rutin dilakukan bisa meningkatkan daya reaksi, konsentrasi, kreativitas dan tentunya kesehatan mental Anda. Hal ini dikarenakan 
				tubuh memompa lebih banyak darah sehingga kadar oksigen dalam peredaran darah juga meningkat yang ujungnya mempercepat pemasukkan darah ke otak.</p>
				<p class="paragraph-medium paragraph-black">Para ahli sepakat jika otak cukup mendapat asupan darah maka reaksi fisik dan mental seseorang akan meningkat.</p>
				
				<p class="paragraph-medium paragraph-black">3. Olahraga dapat melawan penuaan</p>
				
				<p class="paragraph-medium paragraph-black">Sebuah penelitian mengungkapkan bahwa manfaat olahraga yang bisa didapatkan tubuh adalah mencegah penurunan daya kerja otak pada mereka yang sudah lanjut usia. Olahraga ringan yang 
				bisa dilakukan adalah berjalan kaki.</p>
				
				
				<p class="paragraph-medium paragraph-black">4. Olahraga sebagai penyalur saraf otak</p>
				<p class="paragraph-medium paragraph-black">Penelitian menunjukkan bahwa olahraga dapat membuat penambahan gelombang alfa di otak. Gelombang otak alfa sudah lama diketahui berhubungan dengan rileks dan keadaan santai seperti 
				pada waktu bermeditasi.</p>
				<p class="paragraph-medium paragraph-black">Gelombang alfa ini terlihat pada seorang yang joging 20 sampai 30 menit. Para peneliti mengemukakan bahwa bertambahnya kekuatan gelombang alfa memberikan kontribusi kepada keuntungan 
				kejiwaan dari olahraga, termasuk berkurangnya kecemasan dan depresi.</p>
				
				<p class="paragraph-medium paragraph-black">5. Olahraga meningkatkan rasa bahagia</p>
				<p class="paragraph-medium paragraph-black">Manfaat olahraga terbukti manjur dalam meningkatkan hormon penumbuh rasa bahagia dalam otak seperti adrenalin, serotonin, dopamin dan endorphin, yang semuanya merupakan pembunuh 
				nomor satu penyakit hati.</p>
				<p class="paragraph-medium paragraph-black">Sebuah survei di Inggris melaporkan 83% penderita depresi bergantung pada aktivitas olahraga dalam memperbaiki perasaan hati dan mengurangi kecemasan.</p>
				<p class="paragraph-medium paragraph-black">Sementara itu, penelitian yang dilakukan Universitas Duke mengungkapkan, 60% penderita depresi yang menjalani olahraga 30 menit tiga kali seminggu selama enam bulan, dapat melawan 
				penderitaan tanpa harus menggunakan obat dokter.</p>
				<p class="paragraph-medium paragraph-black">Namun bagi penderita depresi yang berat tentu tidak bisa begitu saja lepas dari obat-obatan. Hanya saja banyak dokter sekarang yang memasukkan kegiatan olahraga dalam resep pengobatan
				disamping obat penenang medis.</p>
				</ol>
				<p class="paragraph-medium paragraph-black">Nah, itulah beberapa manfaat yang bisa kita dapatkan dari berolahraga secara rutin. Masihkah kita malas untuk mendapatkan khasiat olahraga?</p>
				
				<p class="paragraph-medium paragraph-black">Source by DokterSehat.</p>
                          
                        
